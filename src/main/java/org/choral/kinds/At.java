package org.choral.kinds;

import java.util.Objects;

public class At extends Kind {
	At() {
	}

	@Override
	public String toString() {
		return "@";
	}

	@Override
	public boolean equals( Object o ) {
		return ( o == this ) || ( o instanceof At );
	}

	@Override
	public int hashCode() {
		return System.identityHashCode( Kind.getAt() );
	}
}
